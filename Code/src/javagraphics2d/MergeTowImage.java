       public  void mergeimg(BufferedImage im, BufferedImage im2) {
        int H = Math.min(im.getHeight(), im2.getHeight());
        int W = Math.min(im.getWidth(), im2.getWidth());
        BufferedImage im3 = new BufferedImage(W, H, BufferedImage.TYPE_INT_ARGB);

        for (int i = 0; i < W; i++) {
            for (int j = 0; j < H; j++) {
                int argb = im2.getRGB(i, j);
                float tr = alph;
                int p = im.getRGB(i, j);
                int a = (int) (((p >> 24) & 0xff));
                int r = (int) (((p >> 16) & 0xff) * tr);
                int g = (int) (((p >> 8) & 0xff) * (tr));
                int b = (int) ((p & 0xff) * (tr));
                p = (a << 24) | (r << 16) | (g << 8) | b;
                argb |= p;
                im3.setRGB(i, j, argb);
            }
        }
        try {
            ImageIO.write(im3, "png", new File(jLabel2.getText()+"\\Alpha.png"));
        } catch (IOException ex) {
            Logger.getLogger(ConvertColorSystem.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
